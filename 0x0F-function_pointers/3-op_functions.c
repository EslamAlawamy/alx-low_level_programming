#include "3-calc.h"
/**
 * op_add - addition fun
 * @a: 1st num
 * @b: 2sec num
 * Return: add
 */


int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtraction fun
 * @a: 1st num
 * @b: 2sec num
 * Return: subtraction
 */


int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplication fun
 * @a: 1st num
 * @b: 2sec num
 * Return: multiplication
 */


int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - division fun
 * @a: 1st num
 * @b: 2sec num
 * Return: division
 */


int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulo fun
 * @a: 1st num
 * @b: 2sec num
 * Return: modulo
 */

int op_mod(int a, int b)
{
	return (a % b);
}
